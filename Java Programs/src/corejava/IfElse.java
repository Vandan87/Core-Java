package corejava;
/* Program's aim is use of if else condition */
public class IfElse {

	public static void main(String args[]) {
		int age = 17;
// 		When age greater than 18 it is eligible
		if (age > 18) {
			System.out.println("You are eligible for job");
		} else {
			System.out.println("Sorry !!! You are not eligible");
		}
	}

}