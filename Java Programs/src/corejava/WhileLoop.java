package corejava;

/* Program's aim is to print 1 to 10 numbers using while loop */

public class WhileLoop {
		
	public static void main(String args[]) {
		int number = 1;
		
//		While loop checked given condition and print number and than increment number
		while (number <= 10) {
			System.out.println(number);
			number++;
		}
	}
	
}
